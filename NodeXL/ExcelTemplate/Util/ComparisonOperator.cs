using System;

namespace Microsoft.NodeXL.ExcelTemplate
{
//*****************************************************************************
//  Enum: ComparisonOperator
//
/// <summary>
/// Specifies how two numbers are compared.
/// </summary>
//*****************************************************************************

public enum
ComparisonOperator
{
    /// <summary>
    /// Less than.
    /// </summary>

    LessThan,

    /// <summary>
    /// Less than or equal to.
    /// </summary>

    LessThanOrEqual,

    /// <summary>
    /// Equal to.
    /// </summary>

    Equal,

    /// <summary>
    /// Not equal to.
    /// </summary>

    NotEqual,

    /// <summary>
    /// Greater than.
    /// </summary>

    GreaterThan,

    /// <summary>
    /// Greater than or equal to.
    /// </summary>

    GreaterThanOrEqual,
}

}
